        . Говорят, что <i>a</i> делится на <i>b </i>нацело, если существует натуральное число <i>c</i>, при умножении
        которого на <i>b </i>получается <i>a</i>:&nbsp;<math xmlns="http://www.w3.org/1998/Math/MathML">
            <mstyle mathsize="22px">
